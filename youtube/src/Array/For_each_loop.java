package Array;

public class For_each_loop {
    static void main(String[] args) {
        // for each loop is used to ittearte through each element
        /*
        syntax
        for(data type name:name of the data sturcture u want to itterate)
         */
        int arr[] ={1,2,3,4,5,15};
        for(int elem:arr){
            System.out.print(elem+" ");
        }
        // for each loop cannot be used to modify the original data structure

    }
}
